package JavBasics;

public class LoopCondition {

	public static void main(String[] args) {
		int i = 10;
		while(i<15) {
			System.out.println(i);
			i++;
		}
		
		System.out.println("For Loop");
		
		for(int j=0; j<10; j++ ) {       /*for(intialization; conditional; increment/decement);{} */       
			System.out.println(j);
			
		}
		

	}

}
